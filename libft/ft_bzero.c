#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*des;

	i = 0;
	des = (char *)s;
	if (n > 0)
	{
		while (i < n)
		{
			des[i] = '\0';
			i++;
		}
		s = (void *)des;
	}
}
